public class FightSong {
    static void statement1()
    {
System.out.println("Go, team, go! \n You can do it.");
    }

    static void statement2()
    {
        System.out.println("You're the best,\n" +
                "In the West.\n");
    }
    static void newline()
    {
        System.out.println(" ");
    }
    public static void main(String[] args)
    {
statement1();
newline();
statement1();
        statement2();
        statement1();
        newline();
        statement1();
        statement2();
        statement1();
        newline();
        statement1();
        statement2();
        statement1();
        newline();
        statement1();
    }
}
